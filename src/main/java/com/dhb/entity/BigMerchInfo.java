package com.dhb.entity;

public class BigMerchInfo {

	private String bigMerchId;
	
	private String keySrc;
	
	private String channelId;
	
	private String url;

	public String getBigMerchId() {
		return bigMerchId;
	}

	public void setBigMerchId(String bigMerchId) {
		this.bigMerchId = bigMerchId;
	}

	public String getKeySrc() {
		return keySrc;
	}

	public void setKeySrc(String keySrc) {
		this.keySrc = keySrc;
	}

	public String getChannelId() {
		return channelId;
	}

	public void setChannelId(String channelId) {
		this.channelId = channelId;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	
	
}
